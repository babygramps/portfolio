/* ============================================================
   PERSISTENCE — one shared board saved to a Lambda + DynamoDB
   backend (same pattern as the BBQ cook-clock). The backend URL
   is discovered at runtime from /colorku/config.json; if it is
   absent or unreachable the app runs purely local (no save).

   Only { seed, difficultyKey, entries, pencil, revealed } is
   stored — generatePuzzle(difficultyKey, seed) rebuilds the exact
   puzzle on load, so the saved doc stays tiny.
   ============================================================ */
const BOARD_DIFFS = new Set(Object.keys(DIFFICULTIES));

async function discoverBoardUrl() {
  try {
    const r = await fetch("/colorku/config.json", { cache: "no-store" });
    if (!r.ok) return null;
    const j = await r.json();
    return j && typeof j.boardUrl === "string" ? j.boardUrl : null;
  } catch {
    return null;
  }
}

// Stable JSON of just the persisted slice; used both to POST and to dedupe
// saves (so cursor moves / "check entries" don't trigger needless writes).
function serializeBoard(seed, difficultyKey, entries, pencil, revealed) {
  return JSON.stringify({
    seed: seed >>> 0,
    difficultyKey,
    revealed: !!revealed,
    entries: entries.slice(),
    pencil: pencil.map((s) => [...s].sort((a, b) => a - b)),
  });
}

// Validate a doc loaded from the backend; null if it can't be used.
function parseBoardDoc(doc) {
  if (!doc || typeof doc !== "object") return null;
  if (!BOARD_DIFFS.has(doc.difficultyKey)) return null;
  if (!Number.isFinite(doc.seed)) return null;
  if (!Array.isArray(doc.entries) || doc.entries.length !== 81) return null;
  if (!Array.isArray(doc.pencil) || doc.pencil.length !== 81) return null;
  return doc;
}

