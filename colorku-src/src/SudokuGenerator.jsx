/* ============================================================
   SUDOKU CORE — bitmask solver, generator, validators
   ------------------------------------------------------------
   Correctness guarantees:
   1. Full grids are generated by randomized backtracking and
      verified as valid permutations of 1–9 in all 27 units.
   2. Clue removal only commits if an exhaustive solution count
      (MRV backtracking, early exit at 2) returns exactly 1.
   3. Easy/Medium additionally require the puzzle to be solvable
      using naked + hidden singles only (human-logic gradable).
   4. A post-generation validation suite re-verifies everything
      from scratch and renders a QC report.
   ============================================================ */

// ---------- seeded PRNG (mulberry32) ----------
function mulberry32(seed) {
  let a = seed >>> 0;
  return function () {
    a |= 0; a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffled(arr, rand) {
  const a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = (rand() * (i + 1)) | 0;
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// ---------- index helpers ----------
const rowOf = (i) => (i / 9) | 0;
const colOf = (i) => i % 9;
const boxOf = (i) => (((i / 9) | 0) / 3 | 0) * 3 + ((i % 9) / 3 | 0);
const ALL = 0x1ff; // bits 0..8 = digits 1..9

function popcount(x) {
  x = x - ((x >> 1) & 0x55555555);
  x = (x & 0x33333333) + ((x >> 2) & 0x33333333);
  return (((x + (x >> 4)) & 0x0f0f0f0f) * 0x01010101) >> 24;
}
const bitToDigit = (bit) => 31 - Math.clz32(bit) + 1;

// 27 units (9 rows, 9 cols, 9 boxes), each a list of 9 cell indices
const UNITS = (() => {
  const u = [];
  for (let r = 0; r < 9; r++) u.push(Array.from({ length: 9 }, (_, c) => r * 9 + c));
  for (let c = 0; c < 9; c++) u.push(Array.from({ length: 9 }, (_, r) => r * 9 + c));
  for (let br = 0; br < 3; br++)
    for (let bc = 0; bc < 3; bc++) {
      const cells = [];
      for (let r = 0; r < 3; r++)
        for (let c = 0; c < 3; c++) cells.push((br * 3 + r) * 9 + (bc * 3 + c));
      u.push(cells);
    }
  return u;
})();

// build row/col/box occupancy masks; returns null on contradiction
function buildMasks(cells) {
  const rows = new Array(9).fill(0);
  const cols = new Array(9).fill(0);
  const boxes = new Array(9).fill(0);
  for (let i = 0; i < 81; i++) {
    const v = cells[i];
    if (!v) continue;
    const bit = 1 << (v - 1);
    const r = rowOf(i), c = colOf(i), b = boxOf(i);
    if ((rows[r] | cols[c] | boxes[b]) & bit) return null;
    rows[r] |= bit; cols[c] |= bit; boxes[b] |= bit;
  }
  return { rows, cols, boxes };
}

/**
 * Exhaustive solution counter with early exit.
 * MRV heuristic: always branch on the empty cell with the
 * fewest candidates. Returns { count: min(actual, limit),
 * solution: first solution found (or null) }.
 */
function countSolutionsEx(cellsIn, limit = 2) {
  const cells = cellsIn.slice();
  const m = buildMasks(cells);
  if (!m) return { count: 0, solution: null };
  const { rows, cols, boxes } = m;
  let count = 0;
  let first = null;

  function dfs() {
    let best = -1, bestMask = 0, bestN = 10;
    for (let i = 0; i < 81; i++) {
      if (cells[i]) continue;
      const mask = ~(rows[rowOf(i)] | cols[colOf(i)] | boxes[boxOf(i)]) & ALL;
      const n = popcount(mask);
      if (n === 0) return;            // dead end
      if (n < bestN) { bestN = n; best = i; bestMask = mask; if (n === 1) break; }
    }
    if (best === -1) {                // grid complete
      count++;
      if (count === 1) first = cells.slice();
      return;
    }
    const r = rowOf(best), c = colOf(best), b = boxOf(best);
    let mask = bestMask;
    while (mask && count < limit) {
      const bit = mask & -mask;
      mask ^= bit;
      cells[best] = bitToDigit(bit);
      rows[r] |= bit; cols[c] |= bit; boxes[b] |= bit;
      dfs();
      cells[best] = 0;
      rows[r] ^= bit; cols[c] ^= bit; boxes[b] ^= bit;
    }
  }
  dfs();
  return { count, solution: first };
}

const countSolutions = (cells, limit = 2) => countSolutionsEx(cells, limit).count;

/**
 * Independent cross-check solver: Knuth's Algorithm X with
 * Dancing Links over the standard sudoku exact-cover matrix
 * (324 constraint columns × 729 candidate rows). Shares no code
 * path with the MRV bitmask solver, so agreement between the two
 * is strong evidence of correctness for both.
 */
function dlxCount(puzzle, limit = 2) {
  if (!buildMasks(puzzle)) return { count: 0, solution: null };
  const NC = 324;
  const maxNodes = 1 + NC + 729 * 4;
  const Lk = new Int32Array(maxNodes), Rk = new Int32Array(maxNodes);
  const Up = new Int32Array(maxNodes), Dn = new Int32Array(maxNodes);
  const Cl = new Int32Array(maxNodes), Sz = new Int32Array(NC + 1);
  const rowID = new Int32Array(maxNodes);
  for (let c = 0; c <= NC; c++) { Up[c] = c; Dn[c] = c; Lk[c] = c - 1; Rk[c] = c + 1; Cl[c] = c; }
  Lk[0] = NC; Rk[NC] = 0;

  let node = NC + 1;
  const rowStart = new Int32Array(729);
  for (let i = 0; i < 81; i++) {
    const r = rowOf(i), c = colOf(i), b = boxOf(i);
    for (let d = 1; d <= 9; d++) {
      const cols = [1 + i, 1 + 81 + r * 9 + d - 1, 1 + 162 + c * 9 + d - 1, 1 + 243 + b * 9 + d - 1];
      const rid = i * 9 + (d - 1);
      rowStart[rid] = node;
      const firstNode = node;
      for (let k = 0; k < 4; k++) {
        const col = cols[k];
        Up[node] = Up[col]; Dn[node] = col; Dn[Up[col]] = node; Up[col] = node;
        Cl[node] = col; Sz[col]++; rowID[node] = rid;
        if (k === 0) { Lk[node] = node; Rk[node] = node; }
        else { Lk[node] = node - 1; Rk[node] = firstNode; Rk[node - 1] = node; Lk[firstNode] = node; }
        node++;
      }
    }
  }

  function cover(col) {
    Rk[Lk[col]] = Rk[col]; Lk[Rk[col]] = Lk[col];
    for (let i2 = Dn[col]; i2 !== col; i2 = Dn[i2])
      for (let j = Rk[i2]; j !== i2; j = Rk[j]) { Up[Dn[j]] = Up[j]; Dn[Up[j]] = Dn[j]; Sz[Cl[j]]--; }
  }
  function uncover(col) {
    for (let i2 = Up[col]; i2 !== col; i2 = Up[i2])
      for (let j = Lk[i2]; j !== i2; j = Lk[j]) { Sz[Cl[j]]++; Up[Dn[j]] = j; Dn[Up[j]] = j; }
    Rk[Lk[col]] = col; Lk[Rk[col]] = col;
  }

  // pre-select rows for the givens (consistency already verified above)
  const work = new Array(81).fill(0);
  for (let i = 0; i < 81; i++) {
    const v = puzzle[i];
    if (!v) continue;
    const n0 = rowStart[i * 9 + (v - 1)];
    let j = n0;
    do { cover(Cl[j]); j = Rk[j]; } while (j !== n0);
    work[i] = v;
  }

  let count = 0;
  let first = null;
  function search() {
    if (Rk[0] === 0) {
      count++;
      if (count === 1) first = work.slice();
      return;
    }
    let col = Rk[0];
    for (let c2 = Rk[0]; c2 !== 0; c2 = Rk[c2]) if (Sz[c2] < Sz[col]) col = c2;
    if (Sz[col] === 0) return;
    cover(col);
    for (let i2 = Dn[col]; i2 !== col && count < limit; i2 = Dn[i2]) {
      const rid = rowID[i2];
      const cell = (rid / 9) | 0;
      for (let j = Rk[i2]; j !== i2; j = Rk[j]) cover(Cl[j]);
      work[cell] = (rid % 9) + 1;
      search();
      work[cell] = 0;
      for (let j = Lk[i2]; j !== i2; j = Lk[j]) uncover(Cl[j]);
    }
    uncover(col);
  }
  search();
  return { count, solution: first };
}

/** Randomized backtracking fill — always succeeds, returns a full valid grid. */
function generateSolution(rand) {
  const cells = new Array(81).fill(0);
  const rows = new Array(9).fill(0);
  const cols = new Array(9).fill(0);
  const boxes = new Array(9).fill(0);

  function dfs() {
    let best = -1, bestMask = 0, bestN = 10;
    for (let i = 0; i < 81; i++) {
      if (cells[i]) continue;
      const mask = ~(rows[rowOf(i)] | cols[colOf(i)] | boxes[boxOf(i)]) & ALL;
      const n = popcount(mask);
      if (n === 0) return false;
      if (n < bestN) { bestN = n; best = i; bestMask = mask; if (n === 1) break; }
    }
    if (best === -1) return true;
    const r = rowOf(best), c = colOf(best), b = boxOf(best);
    const bits = [];
    let mask = bestMask;
    while (mask) { const bit = mask & -mask; mask ^= bit; bits.push(bit); }
    for (const bit of shuffled(bits, rand)) {
      cells[best] = bitToDigit(bit);
      rows[r] |= bit; cols[c] |= bit; boxes[b] |= bit;
      if (dfs()) return true;
      cells[best] = 0;
      rows[r] ^= bit; cols[c] ^= bit; boxes[b] ^= bit;
    }
    return false;
  }
  dfs();
  return cells;
}

/**
 * Constraint-propagation solver restricted to naked singles and
 * hidden singles. Returns the solved grid, or null if those two
 * techniques alone cannot finish (or a contradiction appears).
 * Used to grade whether a puzzle is human-logic solvable.
 */
function solveWithSingles(cellsIn) {
  const cells = cellsIn.slice();
  for (;;) {
    const m = buildMasks(cells);
    if (!m) return null;
    const { rows, cols, boxes } = m;
    let progress = false;
    let complete = true;

    // naked singles
    for (let i = 0; i < 81; i++) {
      if (cells[i]) continue;
      complete = false;
      const mask = ~(rows[rowOf(i)] | cols[colOf(i)] | boxes[boxOf(i)]) & ALL;
      if (mask === 0) return null;
      if (popcount(mask) === 1) {
        cells[i] = bitToDigit(mask);
        const bit = mask;
        rows[rowOf(i)] |= bit; cols[colOf(i)] |= bit; boxes[boxOf(i)] |= bit;
        progress = true;
      }
    }
    if (complete) return cells;

    // hidden singles (per unit, per digit)
    for (const unit of UNITS) {
      let placed = 0;
      for (const i of unit) if (cells[i]) placed |= 1 << (cells[i] - 1);
      let missing = ALL & ~placed;
      while (missing) {
        const bit = missing & -missing;
        missing ^= bit;
        let spot = -1, n = 0;
        for (const i of unit) {
          if (cells[i]) continue;
          const mask = ~(rows[rowOf(i)] | cols[colOf(i)] | boxes[boxOf(i)]) & ALL;
          if (mask & bit) { n++; spot = i; if (n > 1) break; }
        }
        if (n === 0) return null; // digit has nowhere to go
        if (n === 1) {
          cells[spot] = bitToDigit(bit);
          rows[rowOf(spot)] |= bit; cols[colOf(spot)] |= bit; boxes[boxOf(spot)] |= bit;
          progress = true;
        }
      }
    }
    if (!progress) return null; // stuck — needs harder techniques
  }
}

// ---------- difficulty profiles ----------
const DIFFICULTIES = {
  easy:   { label: "Easy",   targetClues: 40, requireSingles: true,  symmetric: true  },
  medium: { label: "Medium", targetClues: 32, requireSingles: true,  symmetric: true  },
  hard:   { label: "Hard",   targetClues: 27, requireSingles: false, symmetric: false },
  expert: { label: "Expert", targetClues: 23, requireSingles: false, symmetric: false },
};

/**
 * Dig holes from a full solution. Every committed removal is
 * re-verified for uniqueness; profile may also demand the puzzle
 * stay solvable with singles only. Returns puzzle + stats.
 */
function digPuzzle(solution, profile, rand) {
  const cells = solution.slice();
  let clues = 81;
  let attempts = 0;

  // removal groups: 180°-rotational pairs (symmetric) or single cells
  let groups;
  if (profile.symmetric) {
    groups = [];
    for (let i = 0; i < 40; i++) groups.push([i, 80 - i]);
    groups.push([40]); // center cell, self-symmetric
  } else {
    groups = Array.from({ length: 81 }, (_, i) => [i]);
  }
  groups = shuffled(groups, rand);

  for (const group of groups) {
    if (clues <= profile.targetClues) break;
    const saved = group.map((i) => cells[i]);
    if (saved.every((v) => v === 0)) continue;
    for (const i of group) cells[i] = 0;
    attempts++;

    let ok = countSolutions(cells, 2) === 1;
    if (ok && profile.requireSingles) ok = solveWithSingles(cells) !== null;

    if (ok) {
      clues -= saved.filter((v) => v !== 0).length;
    } else {
      group.forEach((i, k) => (cells[i] = saved[k]));
    }
  }
  return { puzzle: cells, clues, attempts };
}

// ---------- independent validation suite ----------
function isValidComplete(cells) {
  if (cells.some((v) => v < 1 || v > 9)) return false;
  for (const unit of UNITS) {
    let mask = 0;
    for (const i of unit) mask |= 1 << (cells[i] - 1);
    if (mask !== ALL) return false;
  }
  return true;
}

/**
 * Full validation suite — run on every generated puzzle.
 *   1. structural     solution is a valid permutation in all 27 units
 *   2. cluesSubset    every given matches the stored solution
 *   3. mrvUnique      MRV bitmask solver counts exactly 1 solution
 *   4. solutionMatch  the MRV-found solution equals the stored one
 *   5. dlxUnique      independent DLX exact-cover solver also counts 1
 *   6. solversAgree   MRV and DLX solutions are cell-for-cell identical
 * Plus informational: minimality (every clue individually re-tested
 * for redundancy) and singles-solvability grading.
 */
function fullValidate(puzzle, solution) {
  const structural = isValidComplete(solution);
  const cluesSubset = puzzle.every((v, i) => v === 0 || v === solution[i]);

  const mrv = countSolutionsEx(puzzle, 2);
  const mrvUnique = mrv.count === 1;
  const solutionMatch = mrvUnique && mrv.solution.every((v, i) => v === solution[i]);

  const dlx = dlxCount(puzzle, 2);
  const dlxUnique = dlx.count === 1;
  const solversAgree =
    mrvUnique && dlxUnique && dlx.solution.every((v, i) => v === mrv.solution[i]);

  // minimality: clue i is redundant if uniqueness survives its removal
  let redundantCount = 0;
  const work = puzzle.slice();
  for (let i = 0; i < 81; i++) {
    if (!work[i]) continue;
    const v = work[i];
    work[i] = 0;
    if (countSolutions(work, 2) === 1) redundantCount++;
    work[i] = v;
  }

  const singlesSolvable = solveWithSingles(puzzle) !== null;

  return {
    structural, cluesSubset, mrvUnique, solutionMatch, dlxUnique, solversAgree,
    minimal: redundantCount === 0, redundantCount, singlesSolvable,
  };
}

function checksPass(checks, profile) {
  return (
    checks.structural && checks.cluesSubset &&
    checks.mrvUnique && checks.solutionMatch &&
    checks.dlxUnique && checks.solversAgree &&
    (!profile.requireSingles || checks.singlesSolvable)
  );
}

// ---------- full generation pipeline ----------
function generatePuzzle(difficultyKey, seed) {
  const t0 = performance.now();
  const rand = mulberry32(seed);
  const profile = DIFFICULTIES[difficultyKey];
  const solution = generateSolution(rand);
  const { puzzle, clues, attempts } = digPuzzle(solution, profile, rand);
  const checks = fullValidate(puzzle, solution);
  const timeMs = performance.now() - t0;
  return { puzzle, solution, clues, attempts, checks, timeMs, seed, difficultyKey };
}

